package javaChallenges.practicalFile;
//write a java program to find the sum of natural number
public class naturalNumber {
    public static void main(String[] args) {
        int number = 10;
        int sum = 0;
        for (int i = 0; i <number; i++) {
            sum+=i;
        }
        System.out.println(sum);
    }
}
